name "libgeotiff"
version "1.4.0"

source :url => "http://download.osgeo.org/geotiff/libgeotiff/libgeotiff-1.4.0.tar.gz",
       :md5 => "efa7b418bc00228fcda4da63557e40c2"

dependency 'zlib'
dependency 'libtiff'
dependency 'proj'

relative_path "#{name}-#{version}"

configure_env = {
  "LDFLAGS" => "-L#{install_dir}/embedded/lib -I#{install_dir}/embedded/include",
  "CFLAGS" => "-L#{install_dir}/embedded/lib -I#{install_dir}/embedded/include",
  "LD_RUN_PATH" => "#{install_dir}/embedded/lib"
}

build do
  command "./configure --prefix=#{install_dir}/embedded --with-zlib-prefix=#{install_dir}/embedded", :env => configure_env
  command "make -j #{max_build_jobs}", :env => {"LD_RUN_PATH" => "#{install_dir}/embedded/lib"}
  command "make install"
end
